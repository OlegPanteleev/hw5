package hw5;

public class Staff {

    String fullName;
    String jobTitle;
    String eMail;
    int age;
    int salary;

    Staff(String fullName, String jobTitle, String eMail, int age, int salary){
        this.fullName = fullName;
        this.jobTitle = jobTitle;
        this.eMail = eMail;
        this.age = age;
        this.salary = salary;
    }
}
